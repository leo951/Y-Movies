package com.example.y_movies.models.genre;

public class Genres {
    private int id;
    private String name;

    public Genres(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
